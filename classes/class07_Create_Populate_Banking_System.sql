
/*==============================================================*/
/* Table: TB01_AGENCIA                                          */
/*==============================================================*/
create table TB01_AGENCIA (
   NU_AGENCIA           INT                   not null,
   NO_AGENCIA           VARCHAR2(100)         not null,
