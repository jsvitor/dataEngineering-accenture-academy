'Tabela de Agencias';

/*==============================================================*/
/* Table: TB02_CONTA                                            */
/*==============================================================*/
create table TB02_CONTA (
   NU_AGENCIA           INT                   not null,
   NU_CONTA             INT                   not null,
   CO_CLIENTE_CPF_CNPJ  VARCHAR2(14)          not null,
